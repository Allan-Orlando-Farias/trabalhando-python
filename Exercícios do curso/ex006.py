# DOBRO, TRIPLO E RAÍZ QUADRADA
número = int(input('\033[34mDigite um valor: '))
dobro = número * 2
triplo = número * 3
raíz = número ** (1 / 2)
print('O seu número é\033[31m {}\033[34m, o dobro é\033[31m {}\033[34m, o triplo é\033[31m {}\033[34m e a raíz quadrada é\033[31m {:.2f}'.format(número, dobro, triplo, raíz))
